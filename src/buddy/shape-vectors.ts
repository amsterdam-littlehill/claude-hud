/** Get the pre-computed shape vector for a character. */
export function getShapeVector(ch: string): ShapeVector {
  return CHAR_SHAPE_VECTORS[ch] ?? estimateShapeVector(ch)
}

/** Heuristic shape estimation for characters not in the lookup table. */
function estimateShapeVector(ch: string): ShapeVector {
  const code = ch.codePointAt(0) ?? 0

  // Wide CJK / emoji characters
  if (code >= 0x2E80) {
    return [0.5, 0.5, 0.5, 0.5, 0.5, 0.5]
  }

  // Lowercase
  if (code >= 0x61 && code <= 0x7A) {
    return [0.25, 0.3, 0.2, 0.35, 0.35, 0.3]
  }

  // Uppercase
  if (code >= 0x41 && code <= 0x5A) {
    return [0.35, 0.4, 0.35, 0.35, 0.4, 0.35]
  }

  // Digits
  if (code >= 0x30 && code <= 0x39) {
    return [0.3, 0.4, 0.3, 0.3, 0.4, 0.3]
  }

  return [0.3, 0.3, 0.3, 0.3, 0.3, 0.3]
}

/** Continuous density from shape vector (0.0 = empty, 1.0 = fully covered). */
export function shapeDensity(ch: string): number {
  const [a, b, c, d, e, f] = getShapeVector(ch)
  return (a + b + c + d + e + f) / 6
}

/**
 * Extract a local surface normal from a character's shape vector.
 * Captures intrinsic directionality: '/' leans left, '(' opens right, '^' points up.
 * Returns a unit-length vector for blending with gradient-based normals.
 */
export function shapeNormal(ch: string): [number, number, number] {
  const [tl, tm, tr, bl, bm, br] = getShapeVector(ch)

  // Horizontal imbalance: right-heavy -> positive x
  const x = (tr + 0.5 * tm + br + 0.5 * bm) - (tl + 0.5 * tm + bl + 0.5 * bm)
  // Vertical imbalance: bottom-heavy -> positive y
  const y = (bl + bm + br) - (tl + tm + tr)
  // Depth from total coverage
  const z = Math.max(0.2, shapeDensity(ch) * 2)

  const len = Math.sqrt(x * x + y * y + z * z)
  if (len === 0) return [0, 0, 1]
  return [x / len, y / len, z / len]
}
